import os
import time
import requests
import cv2
import numpy as np

print("Curating Demo Dataset for Judges...")

base_dir = "demo_dataset"
dirs = [
    f"{base_dir}/images/real", f"{base_dir}/images/fake",
    f"{base_dir}/videos/real", f"{base_dir}/videos/fake"
]
for d in dirs:
    os.makedirs(d, exist_ok=True)

def download_image(url, filepath):
    try:
        response = requests.get(url, headers={'User-Agent': 'Mozilla/5.0'}, timeout=10)
        if response.status_code == 200:
            with open(filepath, 'wb') as f:
                f.write(response.content)
            return True
    except Exception as e:
        print(f"Failed to download {url}: {e}")
    return False

# 1. Download 10 Fake Images from ThisPersonDoesNotExist
print("Downloading 10 Fake Images...")
fake_image_paths = []
for i in range(1, 11):
    path = f"{base_dir}/images/fake/fake_face_{i}.jpg"
    # Delay to ensure a new image is generated by TPDNE
    if download_image("https://thispersondoesnotexist.com/", path):
        fake_image_paths.append(path)
    time.sleep(1)

# 2. Download 10 Real Images from RandomUser (stock portraits)
print("Downloading 10 Real Images...")
real_image_paths = []
for i in range(1, 11):
    path = f"{base_dir}/images/real/real_face_{i}.jpg"
    # RandomUser.me uses real stock photos of people
    gender = "men" if i % 2 == 0 else "women"
    if download_image(f"https://randomuser.me/api/portraits/{gender}/{i}.jpg", path):
        real_image_paths.append(path)

# 3. Create Videos from Images function
def create_video_from_image(image_path, output_path, duration_sec=3, fps=30):
    if not os.path.exists(image_path):
        return

    img = cv2.imread(image_path)
    if img is None:
        return
        
    height, width, _ = img.shape
    # Ensure dimensions are divisible by 2 for standard video codecs
    height = height if height % 2 == 0 else height - 1
    width = width if width % 2 == 0 else width - 1
    img = cv2.resize(img, (width, height))

    fourcc = cv2.VideoWriter_fourcc(*'mp4v')
    out = cv2.VideoWriter(output_path, fourcc, fps, (width, height))

    frames = duration_sec * fps
    for i in range(frames):
        # Add a subtle zoom effect to make the video "animated"
        scale = 1.0 + (i * 0.05 / frames)
        
        M = cv2.getRotationMatrix2D((width/2, height/2), 0, scale)
        frame = cv2.warpAffine(img, M, (width, height))
        
        out.write(frame)

    out.release()
    print(f"Created video: {output_path}")

# 4. Create 5 Real Videos using the first 5 Real Images
print("Generating 5 Real Videos from images...")
for i in range(5):
    if i < len(real_image_paths):
        create_video_from_image(real_image_paths[i], f"{base_dir}/videos/real/real_video_{i+1}.mp4")

# 5. Create 5 Fake Videos using the first 5 Fake Images
print("Generating 5 Fake Videos from images...")
for i in range(5):
    if i < len(fake_image_paths):
        create_video_from_image(fake_image_paths[i], f"{base_dir}/videos/fake/fake_video_{i+1}.mp4")

print(f"\nDemo Dataset created successfully in '{os.path.abspath(base_dir)}'!")
